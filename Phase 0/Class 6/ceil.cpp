#include<iostream>
#include<cmath>
using namespace std;

int main() {
    cout << "Ceil of 2.31: " << ceil(2.31) << '\n'; // prints 3
    cout << "Floor of 2.31: " << floor(2.31) << '\n'; // prints 2
    cout << "Round of 2.31: " << round(2.31) << '\n'; // prints 2
    cout << "Round of 2.5: " << round(2.5) << '\n'; // prints 3
    return 0;
}